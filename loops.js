console.log("Print 1 - 5 using for-loop");
for (var i = 1; i <= 5; i++) {
  console.log(i);
}

console.log("Print 1 - 5 using while-loop");
var n = 1;
while (n <= 5) {
  console.log(n);
  n++;
}

console.log("Print 1 - 5 using do-while-loop");
var m = 1;
do {
  console.log(m);
  m++;
} while (m <= 5);
